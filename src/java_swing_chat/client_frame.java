    public void sendFile(String fullPath) {
        //WHOLE NEW LOGIC; OLD IS IN THE .RAR ON DESKTOP
        try {
            Path path = Paths.get(fullPath);
            File file = new File (fullPath);
            byte [] bytes  = Files.readAllBytes(path);
            try ( InputStream in = new FileInputStream(file);) {
                try {
                    ta_chat.append("Sending some files.." + "\n");
                    writer.println(username + ":" + file.getName() + "---" + bytes.length + ":File");
                    writer.flush();
                } catch (Exception e) {
                    e.printStackTrace();
                    ta_chat.append("Message was not sent. \n");
                }

                int count;
                while ((count = in.read(bytes)) > 0) {
                    out.write(bytes, 0, count);
                }

            } catch (IOException e) {
                e.printStackTrace();
                ta_chat.append("Problem reading the file. \n");
            }
        } catch (IOException ex) {
            Logger.getLogger(client_frame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
